namespace phpcord\async;

trait GarbageTrait {
	
	/** @var bool $garbage */
	private bool $garbage = false;
	
	/**
	 * @return bool
	 */
	public function isGarbage(): bool {
		return $this->garbage;
	}
	
	public function setGarbage(): void {
		$this->garbage = true;
	}
}