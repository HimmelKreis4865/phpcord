namespace phpcord\event\role;

use phpcord\event\Event;
use phpcord\guild\permissible\Role;

abstract class RoleEvent extends Event {
	
	public function __construct(private Role $role) { }
	
	/**
	 * @return Role
	 */
	public function getRole(): Role {
		return $this->role;
	}
}